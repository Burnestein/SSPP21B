namespace SSPP21B
{
    //---------------------------------------------------------------------
    //Clase que representa un color.
    //NJSA. 25/08/2021.
    //---------------------------------------------------------------------
    public class CColor
    {
        //---------------------------------------------------------------------
        //Atributos.
        //---------------------------------------------------------------------
        private int Red;
        private int Green;
        private int Blue;

        //---------------------------------------------------------------------
        //Constructor.
        //---------------------------------------------------------------------
        public CColor()
        {
            //Por default el objeto se crea en color negro:

            Red = 255;
            Green = 0;
            Blue = 0;
        }

        //---------------------------------------------------------------------
        //Obtiene el color.
        //---------------------------------------------------------------------
        public string GetColor()
        {
            return "rojo";
        }
    }
}
